/*
 * Print line of binary pattern O's and 1's. 
 */


public class BinaryPattern {

	public static void main(String[] args) { 
		
	    System.out.println(" 0 0 0 0\n" + " 0 0 0 1");
		System.out.println(" 0 0 1 0\n" + " 0 0 1" + " " + "1");
		System.out.println(" 0" + " " +"1 "+"0 0"  ); 
		System.out.println(" 0 1 0 1"); 
		System.out.println(" 0 1 1 0\n" + " 0 1 1 1");
		System.out.println("___________________\n\n" + "16 cols * 4 rows\n" + "___________________\n");
		System.out.println(" 1 0 0 0");
		System.out.println(" 1" + " " + "0" + " " + "0 " + 1 );
		System.out.println(" 1 0 1 0");
		System.out.println(" 1 0 1 1"); 
		System.out.println(" 1 1 0 0\n" + " 1 1 " + "0 " + "1\n" + " 1 1 1 0\n" + " 1 1 1 1");
	}
}
