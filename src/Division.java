/* 
 * What is the remainder and quotient of 10 divisible by 2  
 * 
 */
public class Division {

	public static void main(String[] args) {
		// Division and modulus operator 
		int dividend=10, divisor=2; 
		int remainder = dividend%divisor, quotient = dividend/divisor; 
		
		System.out.println("\n Division and Modulus of 10 divisible by 2 \n " + "Remainder of an int = " +remainder);
        System.out.println(" Quotient = " +quotient);
	}

}
